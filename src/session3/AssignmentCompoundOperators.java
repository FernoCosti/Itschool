package session3;

public class AssignmentCompoundOperators {

    public static void main(String[] args) {
        int number = 10;
        number += 5;
        number = 5 + number;
        System.out.println("After adding 5: " + number);
        number *= 2;
        System.out.println("After multiply by 2: " + 2);
    }
}