package session3.homework;

public class IncrementDecrementHomework {

    public static void main(String[] args) {
        int value =20;
        value++;
        value++;
        value++;
        value++;
        value++;
        System.out.println("After value: " + value);
        int valueHigh = 100;
        valueHigh--;
        valueHigh--;
        valueHigh--;
        valueHigh--;
        valueHigh--;
        System.out.println("After value decrement: " + valueHigh);
    }
}