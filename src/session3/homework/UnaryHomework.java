package session3.homework;

public class UnaryHomework {
    public static void main(String[] args) {
        int num = -50;
        num = -num;
        System.out.println(num);

        int a = -100;
        a = -a;
        System.out.println(a);

        int b = -1000;
        b = -b;
        System.out.println(b);
    }
}